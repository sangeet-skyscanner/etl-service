# Sample unsorted list of numbers
data = [5, 3, 1, 8, 2]

# Sort the list in ascending order (default)
sorted_data = sorted(data)
print(sorted_data)  # Output: [1, 2, 3, 5, 8]

# Sort the list in descending order
sorted_data_desc = sorted(data, reverse=True)
print(sorted_data_desc)  # Output: [8, 5, 3, 2, 1]
